    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        String product_name = txtprd_name.getText();
        String product_pricetxt = txtprd_price.getText();
        
        if (product_name.isEmpty()|| product_pricetxt.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill in all fields PRODUCT NAME  and PRODUCT PRICE");
            return;
        }
        try{
        double price = Double.parseDouble(product_pricetxt);
        prd_listModel.addElement(product_name + "   ₱ " + price );
        price_list.add(price);
        txtprd_name.setText("");
        txtprd_price.setText("");
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Invalid Price input");
        }  
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_computeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_computeActionPerformed
        // TODO add your handling code here:
        double total = 0;
        for (double price : price_list){
            total += price;
        }
        total_label.setText("₱" + total);
        if (price_list.isEmpty()){
            JOptionPane.showMessageDialog(null,"Sorry You dont have any orders yet");
        }
       
        
    }//GEN-LAST:event_btn_computeActionPerformed

    private void btn_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changeActionPerformed
        // TODO add your handling code here:
        double total = 0;
        for (double price : price_list){
            total += price;
        }
       try {
        double payment = Double.parseDouble(txtpayment.getText());

        if (payment >= total) {
            double change = payment - total;
            change_label.setText("₱" + change);
        } else {
            JOptionPane.showMessageDialog(null, "Insufficient payment!");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Invalid Value Input!");
    }
    }//GEN-LAST:event_btn_changeActionPerformed

